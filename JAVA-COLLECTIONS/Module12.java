// HashMap
// En Java, une HashMap est une structure de données qui stocke des paires clé-valeur. 
// Elle implémente l'interface Map<K,V> et permet de retrouver rapidement une valeur à partir de sa clé.
// Importer : import java.util.HashMap; 
// 1. Stockage clé-valeur
// HashMap<String, Integer> ages = new HashMap<>();
// ages.put("Alice", 25);    // Clé: "Alice", Valeur: 25
// ages.put("Bob", 30);

// 2. Accès rapide - O(1) en moyenne
// Integer age = ages.get("Alice");  // Retourne 25