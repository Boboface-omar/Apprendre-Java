// Nous entrons maintenant dans le cœur de Java. 
// Jusqu'ici, nous avons écrit du code en vrac dans le main. Mais Java est un langage Orienté Objet.

// Qu'est-ce qu'un Objet ?
// Imagine que tu es un architecte automobile.

// La Classe (Class) : C'est le plan de fabrication (le plan bleu). 
// Il définit qu'une voiture a 4 roues, un moteur, une couleur, 
// et qu'elle peut rouler. Ce plan ne se conduit pas, c'est juste une définition.

// L'Objet (Object) : C'est la voiture réelle qui sort de l'usine en suivant le plan. 
// Tu peux créer une Ferrari rouge, une Twingo bleue, etc., toutes basées sur le concept de "Voiture".

// ⚙️ La Syntaxe Magique : new
// Pour créer un objet, on utilise le mot-clé new. Ensuite, pour accéder aux variables à l'intérieur de cet objet, on utilise le point (.).

// 1. Création de l'objet (la boîte vide)
// Livre monLivre = new Livre();

// 2. Remplissage de la boîte (affectation des valeurs)
// monLivre.titre = "Le Petit Prince";
// monLivre.pages = 96;

